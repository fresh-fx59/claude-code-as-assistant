#!/bin/bash
set -euo pipefail

usage() {
  echo "Usage: $0 <instance_name> [install_path] [--share-gh-config]"
  echo "Example: $0 codex3"
  echo "Example with shared gh auth: $0 codex2 /usr/local/bin/codex2 --share-gh-config"
}

resolve_user_home() {
  local user
  local home_from_passwd
  user="$(id -un)"
  home_from_passwd="$(getent passwd "$user" 2>/dev/null | cut -d: -f6)"
  if [ -n "$home_from_passwd" ] && [ -d "$home_from_passwd" ]; then
    echo "$home_from_passwd"
    return 0
  fi
  echo "$HOME"
}

resolve_codex_bin() {
  local user_home="$1"
  local user
  local npm_prefix
  user="$(id -un)"
  npm_prefix="$(npm config get prefix 2>/dev/null || true)"

  # Prefer stable installs over transient codex arg0 shims.
  local candidates=(
    "$(command -v codex 2>/dev/null || true)"
    "$user_home/.npm-$user/bin/codex"
    "$npm_prefix/bin/codex"
    "/usr/local/bin/codex"
    "/usr/bin/codex"
  )

  local candidate
  for candidate in "${candidates[@]}"; do
    [ -n "$candidate" ] || continue
    [ -x "$candidate" ] || continue
    case "$candidate" in
      */.codex/tmp/arg0/*)
        # Skip ephemeral wrappers generated by an active Codex session.
        continue
        ;;
    esac
    echo "$candidate"
    return 0
  done

  return 1
}

default_install_path() {
  local instance_name="$1"
  local user_home="$2"
  local system_bin="/usr/local/bin"
  local user_bin="$user_home/.local/bin"

  # If running as root (or /usr/local/bin is writable), prefer global install.
  if [ "$(id -u)" -eq 0 ] || [ -w "$system_bin" ]; then
    echo "$system_bin/$instance_name"
    return 0
  fi

  # Otherwise install in user bin without requiring sudo.
  mkdir -p "$user_bin"
  echo "$user_bin/$instance_name"
}

if [ $# -eq 0 ]; then
  usage
  exit 1
fi

if [ "$1" = "--help" ] || [ "$1" = "-h" ]; then
  usage
  exit 0
fi

INSTANCE_NAME="$1"
shift
USER_HOME="$(resolve_user_home)"

INSTALL_PATH=""
SHARE_GH_CONFIG=false

while [ $# -gt 0 ]; do
  case "$1" in
    --share-gh-config)
      SHARE_GH_CONFIG=true
      ;;
    --help|-h)
      usage
      exit 0
      ;;
    *)
      if [ -z "$INSTALL_PATH" ]; then
        INSTALL_PATH="$1"
      else
        echo "Unknown argument: $1"
        usage
        exit 1
      fi
      ;;
  esac
  shift
done

if [ -z "$INSTALL_PATH" ]; then
  INSTALL_PATH="$(default_install_path "$INSTANCE_NAME" "$USER_HOME")"
fi

TARGET_DIR="$USER_HOME/.$INSTANCE_NAME"
CODEX_BIN="$(resolve_codex_bin "$USER_HOME" || true)"

if [ -z "$CODEX_BIN" ]; then
  echo "codex executable not found in PATH."
  exit 1
fi

if [ -d "$TARGET_DIR" ]; then
  echo "Directory $TARGET_DIR already exists."
else
  echo "Creating directory $TARGET_DIR..."
  mkdir -p "$TARGET_DIR"
fi

if [ -f "$USER_HOME/.gitconfig" ]; then
  echo "Symlinking .gitconfig..."
  ln -sf "$USER_HOME/.gitconfig" "$TARGET_DIR/.gitconfig"
fi

if [ -d "$USER_HOME/.ssh" ]; then
  echo "Symlinking .ssh..."
  ln -sf "$USER_HOME/.ssh" "$TARGET_DIR/.ssh"
fi

if [ "$SHARE_GH_CONFIG" = true ]; then
  if [ -d "$USER_HOME/.config/gh" ]; then
    echo "Symlinking shared gh config..."
    mkdir -p "$TARGET_DIR/.config"
    ln -sf "$USER_HOME/.config/gh" "$TARGET_DIR/.config/gh"
  else
    echo "Skipping shared gh config because $USER_HOME/.config/gh does not exist."
  fi
fi

echo "Installing wrapper to $INSTALL_PATH..."
WRAPPER_CONTENT=$(cat <<EOF
#!/bin/sh
export HOME="$TARGET_DIR"
export XDG_CONFIG_HOME="$USER_HOME/.config"
export XDG_DATA_HOME="$USER_HOME/.local/share"
if [ -z "\${GOG_KEYRING_PASSWORD:-}" ] && [ -f "$USER_HOME/iron-lady-assistant/memory/gog_keyring_password.txt" ]; then
  export GOG_KEYRING_PASSWORD="\$(cat "$USER_HOME/iron-lady-assistant/memory/gog_keyring_password.txt")"
fi
exec "$CODEX_BIN" "\$@"
EOF
)

install_dir="$(dirname "$INSTALL_PATH")"
mkdir -p "$install_dir"

if [ -w "$install_dir" ]; then
  printf "%s\n" "$WRAPPER_CONTENT" > "$INSTALL_PATH"
  chmod +x "$INSTALL_PATH"
else
  printf "%s\n" "$WRAPPER_CONTENT" | sudo tee "$INSTALL_PATH" >/dev/null
  sudo chmod +x "$INSTALL_PATH"
fi

echo "Setup complete for $INSTANCE_NAME."
echo "Wrapper installed at $INSTALL_PATH"
echo "Authenticate with: HOME=$TARGET_DIR codex login"
if [ "$SHARE_GH_CONFIG" = true ]; then
  echo "GitHub CLI auth will be shared from: $HOME/.config/gh"
else
  echo "GitHub CLI auth stays separate. If needed: HOME=$TARGET_DIR gh auth login"
fi
